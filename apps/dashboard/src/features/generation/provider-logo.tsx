const PROVIDER_LOGOS = {
  elevenlabs: ElevenLabsLogo,
  google: GoogleLogo,
  ltx: LtxLogo,
  openai: OpenAiLogo,
} as const

export function ProviderLogo({
  className,
  logoUrl,
  name,
  providerId,
}: {
  className?: string
  logoUrl?: string
  name: string
  providerId: string
}) {
  const Logo = providerId in PROVIDER_LOGOS
    ? PROVIDER_LOGOS[providerId as keyof typeof PROVIDER_LOGOS]
    : null
  const initials = name
    .split(/\s+/)
    .map(part => part[0])
    .join('')
    .slice(0, 2)
    .toUpperCase()

  return (
    <span
      aria-hidden
      className={cn(`
        flex size-6 shrink-0 items-center justify-center overflow-hidden
        rounded-md border border-border/60 bg-muted/70 text-foreground
      `, className)}
    >
      {Logo
        ? <Logo className={providerId === 'ltx' ? 'h-3 w-4' : 'size-3.5'} />
        : logoUrl
          ? <img alt="" className="size-4 object-contain" src={logoUrl} />
          : <span className="text-[9px] font-semibold text-muted-foreground">{initials}</span>}
    </span>
  )
}
